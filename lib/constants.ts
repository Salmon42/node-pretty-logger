/** Red terminal color */
export const R = '\x1b[31m'

/** Green terminal color */
export const G = '\x1b[32m'

/** Blue terminal color */
export const B = '\x1b[34m'

/** Yellow terminal color */
export const Y = '\x1b[33m'

/** Cyan terminal color */
export const C = '\x1b[96m'

/** None terminal color */
export const N = '\x1b[0m'
